"use client";

import { decodeAiReview } from "@/lib/aiReview";
import { Card, CardHeader, CardBody, Badge, Notice } from "@/components/ui";

export function AIReviewDisplay({ aiReview }: { aiReview: `0x${string}` }) {
  const decoded = decodeAiReview(aiReview);
  if (!decoded) return null;

  const { raw, parsed } = decoded;

  return (
    <Card>
      <CardHeader
        title="AI review"
        subtitle="Generated by Ritual AI from all submissions."
        action={
          parsed ? (
            <Badge tone="green">Recommends #{parsed.winnerIndex}</Badge>
          ) : (
            <Badge tone="amber">Unparsed</Badge>
          )
        }
      />
      <CardBody className="space-y-3">
        {parsed ? (
          <>
            {parsed.ranking.length > 0 && (
              <div className="overflow-hidden rounded-xl border border-emerald-500/20">
                <table className="w-full text-left text-sm">
                  <thead className="bg-emerald-500/8 text-xs uppercase tracking-wide text-emerald-600/70 border-b border-emerald-500/15">
                    <tr>
                      <th className="px-3 py-2">#</th>
                      <th className="px-3 py-2">Score</th>
                      <th className="px-3 py-2">Reason</th>
                    </tr>
                  </thead>
                  <tbody>
                    {[...parsed.ranking]
                      .sort((a, b) => b.score - a.score)
                      .map((r) => (
                        <tr
                          key={r.index}
                          className={`border-t border-emerald-500/8 transition-colors ${
                            r.index === parsed.winnerIndex
                              ? "bg-emerald-500/10"
                              : "hover:bg-emerald-500/5"
                          }`}
                        >
                          <td className="px-3 py-2 font-mono text-emerald-300/80">
                            {r.index}
                            {r.index === parsed.winnerIndex && (
                              <span className="ml-1 text-emerald-400">★</span>
                            )}
                          </td>
                          <td className="px-3 py-2 font-medium text-emerald-200">{r.score}</td>
                          <td className="px-3 py-2 text-emerald-300/70">{r.reason}</td>
                        </tr>
                      ))}
                  </tbody>
                </table>
              </div>
            )}

            {parsed.summary && (
              <div className="rounded-xl bg-emerald-500/5 px-3 py-2.5 text-sm text-emerald-200/80 border border-emerald-500/15">
                <span className="text-emerald-600/70 text-xs uppercase tracking-wide font-medium">Summary: </span>
                {parsed.summary}
              </div>
            )}
          </>
        ) : (
          <>
            <Notice tone="amber">
              Couldn&apos;t parse the AI review as JSON. Showing the raw response.
            </Notice>
            <pre className="max-h-72 overflow-auto rounded-xl bg-emerald-500/5 border border-emerald-500/15 p-3 font-mono text-xs text-emerald-300/70 whitespace-pre-wrap break-words">
              {raw}
            </pre>
          </>
        )}
      </CardBody>
    </Card>
  );
}
